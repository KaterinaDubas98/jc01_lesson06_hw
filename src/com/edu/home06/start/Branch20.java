package com.edu.home06.start;

public class Branch20 {

	public static void main(String[] args) {

		int a = 32;
		int b = 49;
		int c = 20;
		int k = 2;
		
		if (a %k == 0) {
			System.out.println(k + " делитель числа " + a);
		} else {
			System.out.println(k + " не делитель числа " + a);
		}
		if (b %k == 0) {
			System.out.println(k + " делитель числа " + b);
		} else {
			System.out.println(k + " не делитель числа " + b);
		}
		if (c %k == 0) {
			System.out.println(k + " делитель числа " + c);
		} else {
			System.out.println(k + " не делитель числа " + c);
		}

	}

}
